// Caculator.js

module.exports.add =
    function add(x, y) {
        return x + y;
    };
module.exports.multiply =
    function multiply(x, y) {
        return x * y;
    };
module.exports.divide =
    function divide(x, y) {
        return x / y;
    };
module.exports.substract =
    function substract(x, y) {
        return x - y;
    };